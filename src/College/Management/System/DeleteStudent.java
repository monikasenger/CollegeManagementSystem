    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
       
         Connection con = Conn.conn();
    int selectedRow = tabledelete.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(null, "Please select a row to delete");
    } else {
        int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this record?", "Confirm", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                String roll = tabledelete.getValueAt(selectedRow, 0).toString(); // Assuming rollno is in column 0

                String query = "DELETE FROM student WHERE rollno = ?";
                PreparedStatement ps = con.prepareStatement(query);
                ps.setString(1, roll);
                ps.executeUpdate();
                ps.close();
                con.close(); // Always close the connection

                load(); // Refresh the table after deletion
                JOptionPane.showMessageDialog(null, "Student record deleted successfully");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    
}
    }//GEN-LAST:event_btndeleteActionPerformed

