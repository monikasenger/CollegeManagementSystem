        String roll=txtroll.getText();
        
        if(roll.isEmpty()){
            JOptionPane.showMessageDialog(null,"Field is Required to search");
        }
        Connection con=Conn.conn();
        
        try {
        String query="select * from student where rollno=?";
            PreparedStatement ps=con.prepareStatement(query);
            ps.setString(1, roll);
            ResultSet rs = ps.executeQuery();
           DefaultTableModel model = (DefaultTableModel) tabledelete.getModel();
            boolean foundData = false;
            // Add data row by row
            while (rs.next()) {
                foundData = true;
                model.addRow(new Object[]{
                    rs.getString("rollno"),
                    rs.getString("name"),
                    rs.getString("class"),
                });
            }
            if(!foundData){
                      JOptionPane.showMessageDialog(null,"Field is Required to");
                    }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
        
